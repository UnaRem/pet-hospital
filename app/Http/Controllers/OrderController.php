    public function createOrder($user_id, $amount)
    {
        $order = new Order();
        $order->user_id = $user_id;
        $order->amount = $amount;
        $order->status = "支付成功";
        if ($order->save()){
            return view('payTip', ['type'=>'success', 'message' => '支付成功！']);
        }
        return view('payTip', ['type'=>'danger', 'message' => '支付失败！']);
    }

